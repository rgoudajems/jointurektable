package testjoin.com.test.kafka;

public class Contact {
String id;
String contact;
public Contact(String id, String contact) {
	super();
	this.id = id;
	this.contact = contact;
}
public String getId() {
	return id;
}
public void setId(String id) {
	this.id = id;
}
public String getContact() {
	return contact;
}
public void setContact(String contact) {
	this.contact = contact;
}


}
